<P align="left"><FONT face=Verdana size=2><B>Sistema de Pontuação</B></FONT></P>
<P align="left"><FONT face=Verdana size=2>O Rating de cada jogador é calculado através da 
fórmula:<BR><BR>XP = ( %V + Dif ) / 10 <BR><BR>XP = Experiência do 
Oponente<BR>%V = Porcentagem de vitórias<BR>Dif = Rating do perdedor - Rating do 
ganhador<BR><BR><BR>Em caso de xeque-mate:<BR>O vencedor ganha: (XPp + 30) / 
2<BR>O perdedor perde: (XPv + 30) / 2<BR><BR>Em caso de empate:<BR>Ambos ganham: 
(XP + 15) / 2<BR><BR>Em caso de rendição:<BR>O vencedor ganha: (XPp + 30) / 
2<BR>O perdedor perde: (XPv + 30) / 2<BR><BR>XPv = Experiência do Vencedor 
<BR>XPp = Experiência do Perdedor</FONT></FONT></P>
<P align="left"><FONT face=Verdana size=2>Exemplo:<BR>- Jogador A (Rating: 1400 e 50% de 
vitórias)<BR>- Jogador B (Rating: 1500 e 80% de vitórias)<BR>Jogador B vence a 
partida por xeque-mate.<BR>XPv = (80 - 100)/10 = -5<BR>XPp = (50 - 100)/10 = 
-2<BR>O jogador A perderá 12 pontos e o jogador B ganhará 14 pontos.</FONT></P>

