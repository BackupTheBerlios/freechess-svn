<P align="left"><B>Sistema de Medalhas</B></P>
<P align="left">O sistema de medalhas funciona da seguinte maneira:</P>
<P align="justify"><B><I>Medalha de Honra: <IMG height=15 src="images/rank/honour.gif" 
width=14 border=0></I><BR></B>- É dada ao jogador que alcançar o primeiro lugar 
no ranking. Todo jogador que ficou em primeiro lugar alguma vez no ranking 
receberá esta medalha.<BR><BR><B><I>Medalhas de Mérito:<br>
</I>( <IMG 
src="images/rank/merit20.gif" border=0 width="8" height="15"> Mérito 20)<br>
( <IMG height=15 
src="images/rank/merit50.gif" width=8 border=0> Mérito 50)<br>
( <IMG height=15 
src="images/rank/merit100.gif" width=8 border=0> Mérito 100)<BR></B>- São dadas ao 
jogador que alcançar determinado número de jogos com 90% ou máis de 
vitórias.<BR>- Mérito 20, 50, 100: Todo jogador que completar 20, 50 ou 100 jogos 
com 90% ou mais de vitórias receberá uma destas medalhas (É possível obter as 
três, mas não mais de uma vez a mesma medalha).<BR><BR><I><B>Medalhas de Coragem:<br>
</B></I><B>( </B><I><B><IMG height=15 src="images/rank/courage70.gif" width=7 border=0> </B></I><B>
Mérito 70)<br>
( </B><I><B><IMG 
height=15 src="images/rank/courage80.gif" width=7 border=0> </B></I><B> Mérito 80)<br>
( </B><I><B><IMG 
height=15 src="images/rank/courage90.gif" width=7 border=0> </B></I><B> Mérito 90)<br>
( </B><I><B><IMG 
height=15 src="images/rank/courage100.gif" width=7 
border=0></B></I><B> Mérito 100)</B><I><B><BR></B></I>- São dadas ao jogador que inicia 10 ou mais jogos no mesmo 
dia e vence pelo menos 70% deles.<BR>- Courage 70, 80, 90, 100: Todo jogador que 
iniciou 10 jogos no mesmo dia e venceu 70%, 80%, 90% ou 100% dos jogos receberá 
uma destas medalhas (É possível obter as quatro, mas não mais de uma vez a 
mesma medalha).<br>
<br>
<I><B>Medalha de Consolação: <img border="0" src="images/rank/purpleheart.gif" width="10" height="15"><br>
</B></I>- É dada ao jogador que obtiver pontuação mínima (1300). Todo
jogador que obtiver esta pontuação alguma vez no seu histórico receberá esta
medalha.</P>

